/**
 * op_sub - subtracts two nums
 * @a: first num
 * @b: second num
 *
* Return: subtraction of a and b
 */
int op_sub(int a, int b)
{
        int minus = a - b;
        return (minus);
}


/**
 * op_mult - multiplies two nums
 * @a: first num
 * @b: second num
 *
 * Return: multiplication of a and b
 */
int op_mul(int a, int b)
{
        int mult = a * b;
	return (mult);
}

/**
 * op_add - adds two nums
 * @a: first num
 * @b: second num
 *
* Return: adds of a and b
 */
int op_add(int a, int b)
{
        int add = a + b;
        return (add);
}

/**
 * op_div - divides two nums
 * @a: first num
 * @b: second num
 *
* Return: divides of a and b
 */
int op_div(int a, int b)
{
        int div = a / b;
        return (div);
}

/**
 * op_mod - modulates two nums
 * @a: first num
 * @b: second num
 *
* Return: modulo of a and b
 */
int op_sub(int a, int b)
{
        int mod = a % b;
        return (mod);
}

